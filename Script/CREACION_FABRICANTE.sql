CREATE TABLE FABRICANTE(
	ID_FAB INTEGER(1),
	NOMBRE VARCHAR(20),
	PAIS VARCHAR(20),
	CONSTRAINT PK_FABRICANTE PRIMARY KEY(ID_FAB)
);
CREATE TABLE PROGRAMA(
	CODIGO INTEGER(2),
	NOMBRE VARCHAR(30),
	VERSION2 VARCHAR(20),
	CONSTRAINT PK_PROGRAMA PRIMARY KEY(CODIGO)
);
CREATE TABLE COMERCIO(
	CIF INTEGER(1),
	NOMBRE VARCHAR(20),
	CIUDAD VARCHAR(20),
	CONSTRAINT PK_COMERCIO PRIMARY KEY(CIF)
);
CREATE TABLE CLIENTE(
	DNI INTEGER(1),
	NOMBRE VARCHAR(20),
	EDAD INTEGER(2),
	CONSTRAINT PK_CLIENTE PRIMARY KEY(DNI)
);
CREATE TABLE DESARROLLA(
	ID_FAB INTEGER(1),
	CODIGO INTEGER(2),
	CONSTRAINT PK_DESARROLLA PRIMARY KEY(ID_FAB,CODIGO),
	CONSTRAINT FK_DESARROLLA FOREIGN KEY(ID_FAB) REFERENCES  FABRICANTE(ID_FAB),
	CONSTRAINT FK2_DESARROLLA FOREIGN KEY(CODIGO) REFERENCES  PROGRAMA(CODIGO)
);
CREATE TABLE DISTRIBUYE(
	CIF INTEGER(1),
	CODIGO INTEGER(2),
	CANTIDAD INTEGER(2),
	CONSTRAINT PK_DISTRIBUYE PRIMARY KEY(CIF,CODIGO,CANTIDAD),
	CONSTRAINT FK_DISTRIBUYE FOREIGN KEY(CIF) REFERENCES  COMERCIO(CIF),
	CONSTRAINT FK2_DISTRIBUYE FOREIGN KEY(CODIGO) REFERENCES  PROGRAMA(CODIGO)
);
CREATE TABLE REGISTRA(
	CIF INTEGER(1),
	DNI INTEGER(1),
	CODIGO INTEGER(2),
	MEDIO VARCHAR(20),
	CONSTRAINT PK_REGISTRA PRIMARY KEY(CIF,DNI,CODIGO,MEDIO),
	CONSTRAINT FK_REGISTRA FOREIGN KEY(CIF) REFERENCES  COMERCIO(CIF),
	CONSTRAINT FK2_REGISTRA FOREIGN KEY(DNI) REFERENCES  CLIENTE(DNI),
	CONSTRAINT FK3_REGISTRA FOREIGN KEY(CODIGO) REFERENCES  PROGRAMA(CODIGO)
);

