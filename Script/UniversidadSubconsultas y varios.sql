FROM ALUMNO_ASIGNATURA A
WHERE A.IDALUMNO NOT IN (SELECT A.IDASIGNATURA 
						FROM ALUMNO_ASIGNATURA A
						WHERE A.IDASIGNATURA =150212
						AND A.IDASIGNATURA=130113);
						
