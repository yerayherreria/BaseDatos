CREATE TABLE EMPLE(
	EMP_NO INTEGER(4),
	APELLIDO VARCHAR(50),
	OFICIO VARCHAR(20),
	DIR INTEGER(4),
	FECHA_ALT DATE,
	SALARIO INTEGER(8),
	COMISION INTEGER(6),
	DEPT_NO INTEGER(2),
	CONSTRAINT PK_EMPLE PRIMARY KEY(EMP_NO)
);

CREATE TABLE DEPART(
	DEPT_NO INTEGER(2),
	DNOMBRE VARCHAR(50),
	LOC VARCHAR(15),
	CONSTRAINT PK_DEPART PRIMARY KEY(DEPT_NO)
);

CREATE TABLE HERRAMIENTAS(
	DESCRIPCION VARCHAR(50),
	ESTANTERIA INTEGER(2),
	UNIDADES INTEGER(2),
	CONSTRAINT PK_HERRAMIENTAS PRIMARY KEY(DESCRIPCION)
);

CREATE TABLE PERSONA(
	COD_HOSPITAL INTEGER(1),
	DNI VARCHAR(9),
	APELLIDOS VARCHAR(50),
	FUNCION VARCHAR(20),
	SALARIO INTEGER(4),
CONSTRAINT PK_PERSONAS PRIMARY KEY(DNI)
);

CREATE TABLE MEDICOS(
	COD_HOSPITAL INTEGER(1),
	DNI INTEGER(8),
	APELLIDOS VARCHAR(50),
	ESPECIALIDAD VARCHAR(20),
CONSTRAINT PK_MEDICOS PRIMARY KEY(DNI)
);

CREATE TABLE HOSPITALES(
	COD_HOSPITAL INTEGER(1),
	NOMBRE VARCHAR(50),
	DIRECCION VARCHAR(50),
	NUM_PLAZAS INTEGER(4),
CONSTRAINT PK_HOSPITALES PRIMARY KEY(COD_HOSPITAL)
);
ALTER TABLE PERSONA ADD CONSTRAINT FK_PERSONAS FOREIGN KEY(COD_HOSPITAL)REFERENCES HOSPITALES(COD_HOSPITAL);
ALTER TABLE MEDICOS ADD CONSTRAINT FK_MEDICOS FOREIGN KEY(COD_HOSPITAL)REFERENCES HOSPITALES(COD_HOSPITAL);

ALTER TABLE EMPLE ADD CONSTRAINT FK2_EMPLE FOREIGN KEY(DEPT_NO)REFERENCES DEPART(DEPT_NO);

